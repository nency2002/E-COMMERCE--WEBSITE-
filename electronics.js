function ui (product) {
    let i = 0;
document.getElementById('sidebarui').innerHTML = product.map((item) => {
    var { image, title, price ,categorly} = item;
    return (
        `<div class="box">
            <div class ="img-box">
                <img  class="images" src=${image}></img>
            </div>    
                <div class="bottom">
                    <h2 >${title}</h2>
                    <h2>$ ${price}.00</h2>
                    <h2>${categorly}</h2>` +
        "<button onclick='addtocart(" + (i++) + ")' >Add To Cart</button>" +
        `</div>
        </div>`
    )
}).join('')
}
ui(product)

// ui ma print
let cartes = [];

function addtocart(a) {
    sidebar = document.querySelector('.sidebar')
    closeicons = document.querySelector('.colse')
    if (sidebar) {
        sidebar.classList.add('active');
    }
    else {
        sidebar.classList.remove('active');
    }
    closeicons.addEventListener('click', () => {
        sidebar.classList.remove('active');
    })
    cartes.push({ ...product[a] });
    displaycart();
}

function delElement(index ) {
    cartes.splice(index, 1);
    displaycart();
}

function displaycart() {
    let j = 0, total = 0;

    document.getElementById("count").innerHTML = cartes.length;

    if (cartes.length == 0) {
        document.getElementById("cartItem").innerHTML = "your cart is empty";
        document.getElementById("total").innerHTML = "$" + 0 + " .00";
    }
    else {
        document.getElementById("cartItem").innerHTML = cartes.map((items) => {
            var { image, title, price, categorly} = items;
            total = total + price;
            document.getElementById('total').innerHTML = "$ " + total + ".00";
            return (
                `<div class = "cart-item">
                    <div class ="row-img">
                        <img class="rowimg" src=${image}></img>
                    </div>
                    <h2 style='font-size:18px;color:black; font-family: sans-serif;'>${title}</h2>
                    <h2 style='font-size:18px;color:red;font-family: sans-serif;'>$ ${price}.00</h2>
                    <h2 style='font-size:18px;color:black;font-family: sans-serif;'>${categorly}</h2>` +
                "<i class='fa-solid fa-trash' onclick='delElement(" + (j++) + ")'></i></div>"
            );

        }).join('');
    }
}


